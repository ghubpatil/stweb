					<div class="entry-content">
						<p>
                            The World Wide Web is a great leveller of sorts. It has destroyed entry barriers crushed bargaining powers and established a new barrier – Adaptive Inclination. Most consumer driven industries are clinically impacted, some are struggling while others are adapting to the new rules of instant gratification. The change is slowly but surely entwining ancillary and B2B industries. Thisin effect has the leadership working round the clock, as strategies are but short-lived and visions-missions are no longer written in stone. Just like the new norm establishes itself, adaptive inclinations and speed are the two biggest assets of the organisation in the world today. Short capsules of time present themselves to take advantage of and if truth be told many perished before Facebook, Uber and Air BnB’s became the successes they are today. Organisations don't only compete with competitors but also with the new ecosystems, newer platforms, de-globalization all of it accelerated by new technology. It doesn't really depend if you were an industry first mover or the last entrant as long as you can effectively leverage the use of current platforms or devise new platforms that allows you to connect directly with the customers. The internet is a free infrastructure to be deployed to overcome entry barriers by creating new verticals that service a customer’s need in time and at an economically acceptable cost. 
                        </p>
                    <p>
                        Just like a WhatsApp forward had recently mentioned Facebook owns no media or uber owns no taxis etc but are growing by the hour and are spread across the world. This makes one apprehensive as well as excited of the cold lightning fast and ever changing environment in which a start-up must grow. When we at Stratacon evaluate a start-up we often add a parameter of adaptive inclination and not just to the current environment but to the future state and this is only because the time to the future is relatively small and shrinking by the day. It is not only technology but also start-ups born out of new technologies are also now challenging industry veterans; a case to point the opportunistic disruption envisaged with Amazon all set to create a mega global delivery system with not only trucks, cargo planes and drones but also people which in all certainty will soon have the UPS’s, DHL’s or FedEx’sreinventing themselves.
                    </p>
                    <p>
                        The ushering in of technologies like VR, AR and AI are positioning a tectonic shift in the way we interact with the world....., which infers everything you know and are comfortable with today is bound to change. To give you an example from large quality control teams and stringent practices we are now helping one of our clients to use application basedAR thatautomates quality check of large machineries to ensure zero faultbefore shipping. This eventuallyensures reduction of manual time in quality checks, expensive logistics in case of returns, increased client satisfaction etc. AR and AI will impact at multiple levels in an organisation from operations to people and customer. It is seamlessly extending the boundaries or just creating new ones. 
                    </p>

                    <p>
                        VR for example is being used extensively as an advertising tool but recently we have been promoting its use for our clients in experiential learning, communications, efficiency enhancement in construction or just your standby city guide in today’s smart cities. AI in all its grandeur from automotive industry to bots and agriculture will create a new threshold for employability. 
                    </p>
                    <p>
                        So how does a start-up navigate in this hostile and challenging environment? According to a recent report “75% of C-level execs say they are currently accelerating investments in AI and AppliedIntelligence.” The good news however is the use of these new technologies will make them more accessible to all and sundry with reduced acquisition costs faster than you would expect. They will as a matter of fact become an enabler for start-ups to pivot quickly, shrink time to market and reduce overheads. 
                    </p>
                    <p>
                        Stratacon has worked with numerous start-ups over the last few years and has studied the success of a lot more to understand what drives success and how start-upsmust navigate themselves in this turbulent and ever changing business environment. 

                    </p>
                    <p>
                        Our UiaEframework – Uncertainty, Innovation, Adaptive Inclination and Entrepreneurship model was developed based on the study of start-ups and has enabled us to build and replicate success stories. 
                    </p>

                    <div class="post-image-wrapper">
						<img src="images/frame.PNG" style="width:670px; height:320px" class="img-fluid" alt="" />
					</div>
                    <p>
                        Uncertainties are inevitable and most competent entrepreneurs abstain from taking the entrepreneurial route because they fail to understand the risks and uncertainties involved. Not being able to visualize the uncertainty compounds the complexity.  Building an understanding and evaluating the factors of uncertainty provides a structure to understand the plausibility of success for a new venture. Through our model we help decision makers to evaluate the ecosystem, capabilities, skills and resources before acting on an opportunity. 
                    </p>
                    <p>
                        Using data analysis and machine learning we train our model to evaluate data feeds and convert qualitative data to quantitative data on a continuous basis. Our goal is to guide start-ups in understanding the field of play the way it is today and the way it will be in a few years whilst bringing to attention that which will be required by the venture to adapt and address rising challenges along the way in creating value. Having a clearer picture on which capabilities to build internally, which to be acquired and which partnerships to be explored in the changing environment provides a clear strategic direction to the new venture in the midst of continuous innovation. 
                    </p>
                    <p style="font-style: italic">-Savio Fernandez is a partner with Stratacon Business Advisory LLP</p>
