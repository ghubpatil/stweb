					<div class="entry-content">
						<p>
                            In a recent client interaction we were asked if our clients were really targeting the right market…. was the customer profiling an accurate representation of their actual customers.... </p>
                        <p>The question in its own right was quite simple,… a look down their documentation on market segmentation, customer profiling, marketing objectives and customer reviews was adequate to determine our position on the question. But the one thing that always stands out each time I approach this subject is the impact of evolving technological innovations on traditional market places and its by-product, the new word "instancy" that demolishes a sustained and ingrained word we call the "target market". The goal posts are changing frequently and before your targeting efforts see the light of day... whoosh there is a new twist to the tale... a word that was coined "disruptive innovation".</p>

                    <p>How do you then address this situation.... just recently the news was in on a certain large brick and mortar retailer inadvertently committing to the impact of the internet on its stores. The footfalls were rising but the units and ticket size both had hit plateau. So its either adapt faster or innovate faster... better still innovate to disrupt.</p>
                <p>Really the teachings of Blue Ocean could never have been more in relevant than now... we have been taught to search new markets, exploit existing ones, narrow it down, dig deeper, wider etc but have we integrated the idea of innovating new markets and customers. Surf the technological wave exploiting the new channels created or continuously create (sustaining) innovative products/services or create a procedural advantage to decrease the impact of changing dynamics (used to be Zara).
                    A procedural advantage is akin to have a procedure in an organisation whose process, approach or market initiation is difficult to emulate. Strong examples of this are visible in the event management industry where firms have been able to create highly profitable properties one after the other that even though is a no brainer cannot be duplicated because of its very uniqueness which is flawless execution at the right time in the right place.
                    </p>

                    <p>What are the strategies to then implement, maintain or innovate a procedural advantage. For this we must encompass the times we live in and how the new life patterns are evolving. From the times of gathering together to watch the favourite TV programs at 7 pm in the 1980’s to being independently engrossed in personal internet enabled devices in 2015’s. The consumer and their lives have changed and so have the markets and the methods to reach these markets. With all of these developments it is imminent that changing strategies in the same markets are no longer a lasting phenomenon. The most striking of this is the speed of change and even more amazing is its adoption. Therefore not only are consumers more adept towards change but are almost looking out for these changes and it is for this simple reason that disruption is the new black. Sustaining innovations is no longer giving the quantum move as is disruption. Just to explain sustaining innovations is a term used to refine and better an existing product or service in the current market position across markets. And therefore how faster is a microprocessor going to get or how longer will a battery’s charged position last. An example could be the innovation depth between the iphone 5 to iphone 7 has it been sustaining or has it been disruptive. iTunes when launched was definitely disruptive and overturned an industry, it created a new market and tuned in a host of loyalists. It gave a new opportunity to consumers of music just like Sony that introduced the pocket radio and enabled teenagers to listen to their favourite music in the environment they preferred. This was a new consumer and a new market within the universe of available markets for the radio in a new avatar to play in. The ecosystem was existing and the plugin was just something that sooner or later someone would have stumbled upon. The established procedure in the ecosystem enabled a procedural advantage by creating the last mile applicability.</p>
                    <p>So is the survival bleak ..... as long as you are able to recognize the market (which actually doesn’t exist and no data can bring it forth) and have one great idea after another that will address this non-existent market with only one factor being the driving force and that is the consumer and the ability to make their life easier the chances of a successful disruption get enhanced.</p>
                    <p>
                        Another factor that is highly rated but under relied on “Vision of the Entrepreneur/Leader” which is imperative to get the ball rolling. Vision also needs to understand and have a calculated plan to some extent of what that invisible market really is. Steve Job was able to create a procedural advantage by using the available ecosystem and develop a single device that sat on top of this ecosystem. Imagine iphone without the internet and mobile apps would it then still be what it is. Following this movement each sustaining innovation through new models only strengthens the brand because of the procedural advantage it holds. Take for instance when they move out of this zone and launch of the apple watch that was extremely complicated and made apps unusually slow till Apple could look through the flaw in its very own procedural advantage and heightened customer experience both of which were compromised. Apple watch was launched with Nike as the products flagship partner targeting health and fitness thereby correcting the flaw in its earlier product and providing an ecosystem along with an umbrella under which use cases can only grow. Now with air pods and the apple watch are we looking at a new disruption a new last mile engagement?
                    </p>
                    <p>
                        Google or Facebook addressed a large market that took off, on the other hand had the market been lousy a top of the class innovation would be only a bystander a case in point is IBM’s Simon introduced in 1992 even before the internet was the disruption to take us to the smartphones as we know them now. However when reality sets in, this is where the acid test plays. Uber is a grand success story of exactly this acid test and its ability to overcome by being a differentiator and low cost at the same time… ring any bells!!! From being founded in 2009 to 250 cities in 50 countries and rising (surfing the disruption wave). Another example is Zip car taking it a step further building efficiency in the existing disruption. Being able to scale up at lightning speed (a procedural advantage) was and is their core competitive advantage.

                    </p>
                    <p>
                        Every new start-up wants to ride the disruption bandwagon, plans to be the next Uber in their industry. But disruption without the bells and whistles always existed and has been the driving force for mankind. Unfortunately the truth is none of these disruptors really knew they were setting out to disrupt an industry, iTunes was created only as a value add to the customer. Hence we don’t create new markets at all in its essence so to speak but we create a solution to the plausible futuristic needs of a customer. This when becomes applicable to a large number of customers and we are in the position to support this increasing mass across geographies and boundaries you are staring in the face of a unicorn who has innovated a new market. This can neither be foretold nor planned it must happen and will be crowned as the disruptive innovator in hindsight because they exploited the new channels created or continuously created innovative products/services or created a procedural advantage to decrease the impact of changing dynamics.
                    </p>
                    
