						<p>Working with SMEs is challenging and exhilarating at the same time. The complexities in small businesses are almost survival. The battle being waged is both in winning work as well as in delivering it. The main agenda for SME owners is to pack in more work in a single day, make every currency unit work to its optimized economic value, focussing on efficiency in sales, production and delivery. Every owner is maximizing his/her output in the hopes of making the organization profitable quickly.</p>
						<p>In a highly charged and time constrained environment the SME owner is looking for instant solutions andtherefore holds little or no inclination to evaluate technologies that aretime consumingand present deferred results. This is precisely the reason why most SMEs abstain from exploring data/business analytics as a beacon for solutions unlike their corporate counterparts.

							This is not to say thatSME owners don’t understand the value of data driven insights but rather think it isn’t for them
							</p>
							<p>This is not to say thatSME owners don’t understand the value of data driven insights but rather think it isn’t for them. 

								So what then, can make SME owners take the plunge to invest in a BI program for their organizations? 
								
								To answer the question we need to explore how BI can help SME entrepreneurs and we need to understand the challenges SME’s face when they set out to use such BI tools. 
							</p>								</br>

								<b>Lack of inclination - </b>Being in the business the SME owners are aware of every change/opportunity in the business, and so,their decision conundrum is, why invest in a technology that tells them what they already know. The lack of inclination in adoptingBI tools therefore can be attributed to lack of visibility on total perceived value and benefits. 
								</br></br>
							<b>Talent & Resources - </b>A SME is generally understaffed and usually compromises on talent in support activities. IT not being one of the primary activities in the value chain especially in non IT based SME’s, exposes lack of technical expertise. Handling security, data-integration, database and server maintenance, making reports and dashboards is challenging in such an environment.
								</br></br>
								<b>Costs & Complexity -</b> When SME’s do evaluate the market for BI tools they are invariably guided or a self-inclined to the full stack of BI tools,thereby leading to a decision almost unopposed as being too expensive and too time consuming. 
							</br></br>
								We believe the answer lies in value and returns, i.e. value derived (qualitative) and returns generated (quantitative). Using the value/return perspective the SME owners can better understand, the transition of a strategic BI adoption to competitive advantage, thereby getting clearer visibility on total gains. 

								</p>

								<div class="post-image-wrapper">
									<img src="images/SME Analytics Values & Return Perspective.png" style="width:670px; height:320px" class="img-fluid" alt="" />
								</div><!-- post image end -->
									<b>	Increased Capabilities - </b>The greatest challenge to an SME owner is to use time effectively and a BI solution does just that. It is not only easy, but also time saving for entrepreneurs to access relevant data (sales figures, contact lists, product/service performance etc.) quickly, run analytics and make business decisions.It helps the SME owner automate most of the tasks in the business andhelps them speed up on critical business decisions enabling their enterprise to be more profitable and accelerate business growth.
									</br></br>
											<b>Analytics Organization - </b>A SME should look at stage wise implementation (including all aspects of business performance in the first phase) of a BI program to reduce both, the costs and gradually build competent resources to manage their BI objective. This approach allows the business to learn while moving ahead in their BI adoption journey. The further stages can then look at more complexfunctions (cooperation, collaboration, financial analytics, SEO, web analytics ICT management, customization, enhanced visualization etc.).
															</br></br>

											<b>Capability Enhancements–</b>Rapid adoption, availability of theinternet and progressive mobilepenetration, has given SME’sunprecedented opportunities in accessing and using data in real time. It builds the organizations knowledge over a period of time facilitating automation, benchmarking (within & outside), and driving efficiency (measures through KPI’s). Above all a good BI program helps the organization in integrating continuous learnings into their strategy making and refining. Enabling the organization to craft strategies at a macro level but translating it with policies and tasks that can be understood at the functional level entwined into their BI program for real time monitoring and effectiveness. The future development can drive performances with outputs from scenario generators, predictive models and interactive visualizations. 
														</br></br>
											<p>Using the above model for understanding business intelligence tools SME’s can get a clear visibility of both short term and long term benefits. 


											Adopting the right BI program for the company is crucial in the success of the program itself. Just a few things a SME owner should keep in mind…
											A BI tool is only as good as the data it accesses. Data veracity, validity and entirety is crucial to see gains from a BI tool. 
											The interface is extremely important and should be intuitive and easy to use. 
											Compatibletowork with limited IT skills. 
											Simple feature inclusions like drop down, slicers, filters, embedded links to ease segmenting, viewing and comprehending data in meaningful terms. 
											Look for a cloud based and mobile friendly BI tools. 
											And evaluate intelligently the best match for required licensing and subscription fees. 
											There are a lot more do’s and don’ts but those listed above, I suppose are key to selecting your BI product. Nothing will surpass getting your research right. Evaluate the tool going through each drop down list and correlating it with your business needs with a “do I need it” or “I don’t need this” or “I need something that can help me with this but it isn’t on the select list”.

