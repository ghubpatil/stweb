						<p>
                            Going down the entrepreneurial road is not only creative, exciting and full of surprises but is also strewn with innumerable challenges. Whilst it’s tough,failure is also a norm, for every successful startup there are at least five sad stories. There is no formulae for success but it must always begin with a fantastic idea that has a market and hard work along with some opportunity creation, innovation, ingenuity, foresight and pivots. The only recurring patternof successful businesses is that the founders have always in one way or the other tested their idea for its acceptance, believed in it and worked really hard without taking their eye of the unit economics.
                        </p>
                        <p>
                            KPMG’s global data states CY Q3 2015 VC backed companies raised $37.6B across 1799 deals. Asia has been witnessing high levels of investing activity and has pulled ahead of Europe in the last 5 Qtrs by about $28.2 B. The Indian start up scene is searingand has been particularly persistent in raising funds topping $ 2B three times in 4 Qtrs. Thousands of proposals pour in and start up accelerators, incubators and investor groups are mushrooming by the day. 
                        </p>
                    <p>
                        India today is the right place to be in, it has the opportunity, critical mass, investors and talent. But the hard truth is thateight or nine out of ten startups fail. And through this post I wanted to list what really makes a startup successful. Really I will not be attempting to provide a tick the box scenario but rather work across uncovering the broader elements that enables and motivates the ecosystem to support a new business entity and help it on its road to success. 
Repeatedly experience has proven that there is One and only one Element

                    </p>
                    <p><b>
                        Finding Funding and Finding it with the right Investors at the right time!!!!!</b>
                    </p>
                    <p>
                        I think the top of all reasons of startups that succeeded found its roots in the fact that they were able to source funding at the right time with the right investors. Investors that actively take interest to see that the startup succeeds bring a lot to the table. It is essential for the investors to understand and resonate with the vision, work towards it with the business owners for a win-win situation. There are many investors/angels who have the cash but lack the experience since they are involved with their own businesses on a daily basis and these are the ones that can’t value add the startup but rather work with the numbers and hope to see one or two successful exits from their portfolio of investments.
                    </p>
                    <p>
                        Now this may have surprised you, as a startup can’t be successful only because of element 1. Well I would say that’s it, because the rest of it below is a pre-requisite a must have and without which you would never reach Element 1 to begin with… unless of course you have your own funds to employwhich means again you won’t be needing Element.
                     </p>
                    <p>
                       <b> Need and Fit!!!-</b>First test your hypotheses with real customers, the ones you will eventually need to sell to..
                        There has to be a need for your product/service and the alignment of your product/service with the need must fit precisely!!! … e.g. SMS Noodle’s an SMS based entertainment channel for Singapore having its roots at the iAccelerator 2008 camp IIM-A. The founder Vijay Ganesan states they failed primarily because they overestimated the Singaporean market. Though the Singaporeans are SMS crazy they weren’t willing to pay for the service. 
                        Rather than work product first review your idea/concept and work from the most attractive target marketand see if your idea can be pivoted to address your consumer (never get married to your initial idea). Also as Vijay has stated bounce of your idea to as many people as you can to get a validation you will find get a lot of information and some of them are eye openers a full extract can be found here .
                        There is a vast number of startups that failed because of miscalculations on Need and fit, Lumos, Rate <a href=" https://www.globallinker.com/bizforum/article/savio-fernandez-partner-stratacon-business-advisors-llp/35251/#/overlay/signup/articleview/35251">My Speech </a>, Group Spaces, Kolos, intellibanketc, 
                        Shift till you find the right solution for the right need !!!!!
                        
                    </p>
                    <p>
                        <b>Growth Rate (scale up )</b>Imperative, it keeps both the investors and the founders positive and energized. You have limited resources so you have to launch and grow super quickly and I wouldn’t shy to say “instantly”. Indepth research states the top 15- 20% of startups in terms of their year one revenue have a greater opportunity to stay ahead then those in the middle and those in the bottom 20% usually never ever reach the top slot e.g. Shwrüm, Stipple,Layervault, Shnergle, Brawkeretc
                        Evaluate your startup idea and work your business model to build revenue streams early on with a clear focus on your customer. Again I reiterate validate your business idea and revenue model with your end customer and whether they will be willing to pay for your product. Scan the external environment and evaluate the threats that can disrupt your model. Speed will be essential to bring your product to the market once validated. The longer you take the higher chances that your firm may never see the end of the tunnel… technology is innovating every day.
                        
                    </p>
